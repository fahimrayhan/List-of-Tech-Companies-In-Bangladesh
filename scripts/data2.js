const companiesPerPage = 9; // Number of companies to display per page
let currentPage = 1; // Current page number

function displayCompanies(companies) {
  const companyList = document.getElementById('companyList');
  companyList.innerHTML = '';

  const startIndex = (currentPage - 1) * companiesPerPage;
  const endIndex = startIndex + companiesPerPage;

  const companiesToDisplay = companies.slice(startIndex, endIndex);

  companiesToDisplay.forEach(company => {
    const card = document.createElement('div');
    card.className = 'col';

    card.innerHTML = `
    <div class="card h-100">
        <div class="card-body">
        <h4 class="card-title company_name">${company.company_name}</h4>
          
          <p class="techStack mt-4"><strong>Tech Stacks:</strong> ${company.company_techstack}</p>
          <p class="location"><strong>Address:</strong> ${company.company_address}</p>
          <a class="btn btn-primary align-baseline" href="${company.company_website}" target="_blank">Website</a>
        </div>
      </div>
    `;

    companyList.appendChild(card);
  });

  updatePagination(companies.length);
}

// Pagination
function updatePagination(totalCompanies) {
  const totalPages = Math.ceil(totalCompanies / companiesPerPage);
  const pagination = document.querySelector('.pagination');

  pagination.innerHTML = '';

  const maxVisiblePages = 5; // Maximum visible page numbers
  const halfMaxVisiblePages = Math.floor(maxVisiblePages / 2);
  let startPage = Math.max(1, currentPage - halfMaxVisiblePages);
  let endPage = Math.min(startPage + maxVisiblePages - 1, totalPages);

  if (endPage - startPage < maxVisiblePages - 1) {
    startPage = Math.max(1, endPage - maxVisiblePages + 1);
  }

  const createPaginationLink = (pageNumber, text) => {
    const pageItem = document.createElement('li');
    pageItem.className = 'page-item';
    
    if (pageNumber === currentPage) {
      pageItem.classList.add('active');
    }

    const pageLink = document.createElement('a');
    pageLink.className = 'page-link';
    pageLink.href = '#';
    pageLink.textContent = text;
    pageLink.addEventListener('click', () => {
      currentPage = pageNumber;
      displayCompanies(Companies);
    });
    pageItem.appendChild(pageLink);
    return pageItem;
  };

  if (currentPage > 1) {
    pagination.appendChild(createPaginationLink(1, 'First'));
    // const prevPageItem = createPaginationLink(currentPage - 1, 'Prev');
    // pagination.appendChild(prevPageItem);
  }

  for (let i = startPage; i <= endPage; i++) {
    const pageItem = createPaginationLink(i, i);
    pagination.appendChild(pageItem);
  }

  if (currentPage < totalPages) {
    // const nextPageItem = createPaginationLink(currentPage + 1, 'Next');
    // pagination.appendChild(nextPageItem);
    pagination.appendChild(createPaginationLink(totalPages, 'Last'));
  }
}


function changePage(page) {
  currentPage = page;
  displayCompanies(Companies);
}

document.getElementById('locationFilter').addEventListener('change', function() {
  currentPage = 1; // Reset page to 1 when changing filters
  const selectedLocation = this.value;
  const filteredCompanies = filterCompanies(selectedLocation);
  displayCompanies(filteredCompanies);
});

document.getElementById('prevPage').addEventListener('click', function(e) {
  e.preventDefault();
  if (currentPage > 1) {
    changePage(currentPage - 1);
  }
});

document.getElementById('nextPage').addEventListener('click', function(e) {
  e.preventDefault();
  const totalPages = Math.ceil(Companies.length / companiesPerPage);
  if (currentPage < totalPages) {
    changePage(currentPage + 1);
  }
});

// Filter Companies by Location
function filterCompanies(location) {
  if (!location) {
    return Companies;
  }
  return Companies.filter(company => company.company_address.includes(location));
}


document.getElementById('locationFilter').addEventListener('change', function () {
  currentPage = 1; // Reset page to 1 when changing filters
  const selectedLocation = this.value;
  const filteredCompanies = filterCompanies(selectedLocation);
  displayCompanies(filteredCompanies);
});

// Search By Name

function searchCompanies(query) {
  query = query.toLowerCase();
  return Companies.filter(company => company.company_name.toLowerCase().includes(query));
}

document.getElementById('companySearch').addEventListener('input', function () {
  const searchTerm = this.value.trim();
  if (searchTerm !== '') {
    const searchResults = searchCompanies(searchTerm);
    displayCompanies(searchResults);
  } else {
    displayCompanies(Companies);
  }
});

