function createTechStackOptions() {
  const techStackOptions = document.getElementById('techStackOptions');
  techStacks.forEach(tech => {
    const checkboxDiv = document.createElement('div');
    checkboxDiv.className = 'form-check';

    const checkbox = document.createElement('input');
    checkbox.type = 'checkbox';
    checkbox.className = 'form-check-input techStackFilter';
    checkbox.id = 'techStack' + tech.replace(/\s+/g, ''); // Remove spaces for the ID
    checkbox.value = tech;

    const label = document.createElement('label');
    label.className = 'form-check-label';
    label.htmlFor = 'techStack' + tech.replace(/\s+/g, ''); // Remove spaces for the ID
    label.textContent = tech;

    checkboxDiv.appendChild(checkbox);
    checkboxDiv.appendChild(label);
    techStackOptions.appendChild(checkboxDiv);
  });
}

//Filter by tech stacks

function filterCompaniesByTechStack(selectedTechStacks) {
  if (selectedTechStacks.length === 0) {
    return Companies;
  }
  
  return Companies.filter(company => {
    const companyTechStacks = company.company_techstack.split(',').map(tech => tech.trim());
    return selectedTechStacks.every(selectedTech => companyTechStacks.includes(selectedTech));
  });
}

// Function to update the list of displayed companies
function updateCompanyList() {
  const selectedTechStacks = Array.from(document.querySelectorAll('.techStackFilter:checked')).map(checkbox => checkbox.value);
  console.log('Selected Tech Stacks:', selectedTechStacks);
  const filteredCompanies = filterCompaniesByTechStack(selectedTechStacks);
  displayCompanies(filteredCompanies);
}

